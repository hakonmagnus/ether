section .entry
global _start

bits 32

;=============================================================================;
; _start                                                                      ;
; 32-bit entry point                                                          ;
; @param EAX = Magic                                                          ;
; @param EBX = Pointer to multiboot info struct                               ;
;=============================================================================;
_start:
    cli
    mov ax, 0x10
    mov ds, ax
    mov es, ax
    mov fs, ax
    mov gs, ax
    mov ss, ax
    mov esp, 0x90000

    mov edi, 0xB8000
    mov ah, 0x0F
    mov al, 'A'
    mov word [edi], ax

    cli
    hlt
