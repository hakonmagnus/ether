bits 32

mm_next dd loader_end

;=============================================================================;
; mm_alloc                                                                    ;
; Allocate a block of a given size                                            ;
; @param EAX = Size of block                                                  ;
; @return EAX = Pointer to block                                              ;
;=============================================================================;
mm_alloc:
    push ebx

    mov dword ebx, [mm_next]
    add dword [mm_next], eax
    mov eax, ebx

    pop ebx
    ret
