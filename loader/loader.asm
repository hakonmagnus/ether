org 0x500
bits 16

start:
    cli
    xor ax, ax
    mov ds, ax
    mov es, ax
    mov fs, ax
    mov gs, ax
    mov ss, ax
    mov sp, 0xFFFF
    sti
    
    mov ah, 0x0E
    mov al, 'A'
    int 0x10
    
    cli
    hlt